package it.trefin.erecruitment.model;

public class TotalFeedback {
	Integer numColloqui,totPositivi,totNegativi;

	public Integer getNumColloqui() {
		return numColloqui;
	}

	public void setNumColloqui(Integer numColloqui) {
		this.numColloqui = numColloqui;
	}

	public Integer getTotPositivi() {
		return totPositivi;
	}

	public void setTotPositivi(Integer totPositivi) {
		this.totPositivi = totPositivi;
	}

	public Integer getTotNegativi() {
		return totNegativi;
	}

	public void setTotNegativi(Integer totNegativi) {
		this.totNegativi = totNegativi;
	}
	
	
}
